						<p>Les nouvelles technologies et Internet en particulier sont à la base d'une (r)évolution qui est en passe de bouleverser la société autant que l'imprimerie au XVe siècle. Le Parti Pirate est un mouvement international qui veut s'engager en politique pour que la société, et la sphère politique en particulier, intègre ces changements radicaux. Pour ce faire le, Parti Pirate propose une philosophie que l'on peut résumer comme suit. </p>
						
						<p><ol class="marginLeft25">
							<li><p>La liberté d’expression n’a jamais été aussi grande et à la fois autant menacée que depuis l’apparition d’Internet. De même, l’information n’a jamais été aussi abondante et paradoxalement peu ciblée sur la politique. Le Parti Pirate souhaite assurer le droit d’expression, mais aussi le droit d’information à chacun. Ceci passe nécessairement par la <b>transparence des institutions publiques</b><sup><a href="#note1">(1)</a></sup> et par <b>le respect de la vie privée</b> pour permettre à chacun de se faire un avis, de le communiquer et de l’échanger en toute sécurité</p></li>
							<li><p>Aujourd'hui, les nouvelles technologies permettent réellement de favoriser la collaboration et donc l'initiative citoyenne<sup><a href="#note2">(2)</a></sup>, Le Parti Pirate pense que les citoyens sont capables de construire ce qu’ils souhaitent, pour eux-mêmes et pour le bien commun, si on leur en donne les moyens. C’est pourquoi <b>les idées, les outils, la culture et les connaissances</b> doivent pouvoir être <b>accessibles à tous et partagés</b>. De même, <b>l’éducation</b> (au sens large) et <b>la prise en main des citoyens par eux-mêmes (empowerment)</b> doit être une priorité à tout niveau.</p></li>
							<li><p>Enfin, les pirates pensent que l’objectif principal des pouvoirs publics est de mettre en place un système résilient, c’est-à-dire solide et cohérent, à l’aide d’une <b>législation de qualité</b>. Une loi est là pour résoudre un problème, pas pour en créer de nouveaux, ni pour refléter une idéologie. Si la démocratie directe n’est pas une solution aujourd’hui, le Parti Pirate souhaite néanmoins favoriser la participation citoyenne. La <b>collaboration</b> et le partage d’idées dans la <b>cohérence et le respect de chaque individu</b> ainsi que la <b>diversité</b> - des personnes, des cultures, des points de vue, des origines - sont des éléments essentiels de cette construction citoyenne.</p></li>
						</ol></p>
												
