						<p>Parce que “Pirate” est un sobriquet destiné à nous ridiculiser... Nous avons le sens de l’humour et la confiance que nous ne sommes pas ridicules&nbsp;! C’est pourquoi nous garderons ce nom. Ahoy&nbsp;! Á l’abordage&nbsp;!
						</p>
						
						<a class="top" href="#top" name="hommes">&nbsp;</a>
						<h4>Pourquoi vous n’avez que des hommes dans votre parti&nbsp;?</h4>
						
						<p>S’il y a une majorité d’homme chez nous, c’est sans doute dû à l’histoire du mouvement pirate. En effet, à l’origine, la vision du monde des Pirates est inspirée, entre autres, par le monde OpenSource, qui est propre à l’informatique, et où il y a effectivement beaucoup d’hommes. Cependant, nous croyons justement que le Parti Pirate est appelé à avoir beaucoup de femmes dans ses rangs, quand nos idées seront plus connues. En effet, nous proposons une approche de la politique qui n’est plus celle du “combat des chefs” pour le pouvoir, mais plutôt une approche de collaboration, de cohérence et de participation citoyenne, et beaucoup de sociologues attribuent ces concepts aux femmes plutôt qu’aux hommes...
						</p>
						
