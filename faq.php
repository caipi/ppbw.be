						<p>Au contraire, si quelqu'un veut pirater nos idées, qu'il ne se gène pas. Nous sommes là pour faire passer ces idées dans la population pour que le monde change. Si ces idées sont portées par d'autres partis, tant mieux&nbsp;! Faisons juste attention que ce ne soit pas un petit mensonge pour amasser plus de voix.&nbsp;:-) Gardons l'oeil critique ouvert.
