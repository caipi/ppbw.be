						<h3>Notre vision du monde</h3>
						
						<a class="top" href="#top" name="auteur">&nbsp;</a>
						<h4>Le droit d’auteur est quand même important. Pourquoi voulez-vous le supprimer&nbsp;?</h4>
						
						<p >Le droit d’auteur, et plus généralement la propriété intellectuelle, est importante et nous ne souhaitons pas le supprimer. Nous souhaitons réformer en profondeur ce système car il ne répond plus aux attentes.
						Si nous prenons les exemples des brevets, que voyons-nous aujourd’hui&nbsp;? Lors de procès entre géants (Apple, Samsung, Google ou Motorola, pour ne citer qu'eux), ils servent d’armes pour du chantage (“Je sors un brevet que tu ne respectes pas si tu m’attaques") plutôt que pour une réelle protection d'une "idée". De plus, on ne compte plus les exemples où le brevet entrave l'innovation (lien vers article de Ploum).
						Quant au domaine de la musique et de l'art en général, notre souhait est de rétribuer au mieux les artistes, plutôt que de payer l'industrie du copyright par le maintien de barrières économiques factices (proposées par les majors). Une comparaison facile&nbsp;: les vendeurs de glace ont disparus avec la démocratisation des réfrigérateurs. Mais l'industrie du froid (ceux qui font des glaces et ceux qui font des réfrigérateurs) se porte très bien&nbsp;! Pourquoi ne pouvons-nous pas trouver une alternative pour la musique&nbsp;? Les outils techniques existent.
						</p>
						
						<a class="top" href="#top" name="directe">&nbsp;</a>
						<h4>En fait, vous prônez la démocratie directe&nbsp;? C’est très dangereux, non&nbsp;?</h4>
						
						<p>La démocratie directe est un outil qui, appliqué aujourd’hui, pourrait être dangereux. Des groupes mal intentionnés pourraient faire voter des lois contre les homosexuels, pour la peine de mort, etc. Voyez en Suisse&nbsp;: les minarets sont interdits car leur système de votation est très direct. Cependant, les choses évolueront et nous croyons qu’à long terme, il sera possible d’avoir un système - nous ne savons encore pas lequel - qui permettra à chacun de s’impliquer, de s’engager et de participer en politique. Et nous croyons aussi que c'est la technologie, combiné à une évolution des mentalités, qui permettra ce changement. En pratique, aujourd’hui, les pirates proposent de garder ce but d’engagement et de participation citoyenne. À notre époque, cela ne se traduit pas par une démocratie directe, mais plutôt par des forums citoyens, des rencontres et des discussions réelles avec les élus, mais aussi une humilité de ceux-ci et du parti qui ne connait pas “tout sur tout” et qui en réfère aussi aux gens pour obtenir un avis.</p>
						
						<h3>Le Parti Pirate</h3>
						
						<a class="top" href="#top" name="difference">&nbsp;</a>
						<h4>Quelle est la différence entre le Parti Pirate et les autres partis&nbsp;?</h4>
						
						<p>Nous envisageons la politique différemment. De même que l’imprimerie a révolutionné le monde et a participé au début de la renaissance, Internet et les nouvelles technologies opèrent une véritable révolution. Il faut en tenir compte car le monde change radicalement. Les politiques actuels n’en tiennent pas compte. Nous ne voulons pas supprimer les différences gauche-droite, mais nous voulons qu’elles prennent en compte cette révolution. Cette révolution permet en plus à chaque citoyen d’avoir un rôle à jouer dans la société. Cela passe par la transparence (le point de départ pour savoir de quoi on parle) et la participation.</p>
						
						<a class="top" href="#top" name="systeme">&nbsp;</a>
						<h4>Que reprochez-vous au système politique actuel&nbsp;?</h4>
						
						<p>Certainement pas les idées, mais bien la démarche. En effet, les idées politiques actuelles sont -généralement- bonnes et les pirates pourraient proposer les mêmes. Mais pas par le même chemin. En effet, dans un parti traditionnel, une proposition vient d’une idéologie partisane, parfois un peu figée. Ce que nous proposons, c’est une absence d’idéologie fixe&nbsp;: les pirates ont une vision du monde et de là découle que les gens ont leur mot à dire. Fini d’opposer les idées&nbsp;: partageons-les et que chacun puisse en proposer&nbsp;! Pensez à Wikipedia. Il n’y a pas d’idéologie&nbsp;; seulement une vision de partage de la connaissance. Pas de pouvoir qui dit “il faut travailler sur ceci ou cela”. Les gens collabore où ils l’entendent, comme ils l’entendent, en respectant quelques règles. C’est un modèle comme ceci qu’il faut trouver pour la démocratie et c’est ce que les pirates proposent.</p>
						
						<a class="top" href="#top" name="populiste">&nbsp;</a>
						<h4> Comme tous les petits partis, vous dénigrez les autres partis. Êtes-vous populistes / poujadistes&nbsp;?</h4>
						
						<p>Pas du tout. Notre message n’est pas que nous sommes meilleurs que les autres partis. Leurs idées ne sont pas mauvaises. Simplement, nous souhaitons prendre le meilleur de chacun, sans idéologie partisane (ou imposée). Notre approche est différente d’une approche gauche droite classique. Nous aurons peut-être certaines propositions de solutions en commun. Mais notre idée est d’arrêter d’opposer les idées et d’en faire une division. Partageons-les, travaillons-les ensemble et prenons les meilleures&nbsp;; collaborons et soyons pragmatiques. </p>
						
						<a class="top" href="#top" name="utile">&nbsp;</a>
						<h4>Ca ne sert à rien de voter pour un petit parti, je préfère voter pour un gros, pour que mon vote soit utile. </h4>
						
						<p>Nous ne sommes pas juste un “petit parti”. Nous nous inscrivons dans un mouvement international. Né en Suède, il a déjà ses premiers succès en Allemagne (jusqu’à 10% selon les Länder) et au parlement européen. Des partis pirates naissent dans la plupart des pays européens et dans beaucoup d'autres dans le monde&nbsp;! N’oublions pas que les socialistes sont entrés en politiques il y a 100 ans, les écolos il y a 40 ans. Nous commençons maintenant, mais nous représentons l’avenir.</p>
						
						<a class="top" href="#top" name="hierarchie">&nbsp;</a>
						<h4>Est-ce vrai qu'il n'y a pas de hiérarchie dans le Parti Pirate&nbsp;?</h4>
						
						<p>Oui et non. Oui car chaque pirate ou chaque groupe de pirates est autonome. C'est une excellente manière de promouvoir les idées, les initiatives. Cela favorise la collaboration et la participation citoyenne. La preuve, vous êtes sur le site du Parti Pirate Brabant Wallon, qui s'est fixé comme objectif d'expliquer les idées Pirates aux gens qui le souhaitent (et nous espérons que ces pages y aident). Mais il y a bien sûr aussi quelques règles. Les premières sont celles du Parti Pirate de Belgique, au nombre de <a href="http://pirateparty.be/?page_id=144&lang=fr" class="external text" rel="nofollow">trois</a>. Et il y a aussi le principe du consensus qui, autant que possible, doit être trouvé. Cela évite certains abus (majorité contre minorité) et certaines dérives. 
						Pour résumer, il y a bien une structure de coordination du Parti Pirate, mais la place est laissée à l'autonomie afin de laisser chacun participer autant qu'il le veut. Et au fait, vous êtes bienvenus&nbsp;!
						</p>
						
						<a class="top" href="#top" name="situation">&nbsp;</a>
						<h4>Où vous situez-vous sur l'échiquier politique&nbsp;?</h4>
						
						<p>A droite, à gauche, au milieu, au-dessus et en-dessous. Ceux qui vous disent que nous sommes des communo-anarchistes ou des dangereux libertaires de droite (pour autant que ces catégories existent) se trompent. En effet, c'est la "vision du monde" qui définit la "position" d'un parti. Mais comme vous l'aurez compris en lisant ce site, notre vision est fondamentalement transversale. Elle sous-tend les idées, qu'elles soient de droite ou de gauche. Comme le Parti Pirate promeut l'engagement individuel et la participation citoyenne, les idées seront de droite ou de gauche, en fonction du besoin, en fonction du problème rencontré. Est-ce à dire que nous n'avons pas d'idées précises&nbsp;? Au contraire, nous en avons beaucoup (voyez nos programmes), mais elles ne sont pas fixes et universelles. Elles ne sont pas applicables aveuglément à tous les niveaux de pouvoir et dans toutes les situations.
						</p>
						
